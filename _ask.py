import json, io
import server as s
def q(text, limit=20):
    r = s.execute_query(text, None, limit)
    try: return json.loads(r)
    except Exception: return {"_raw": r}
out = {}
out['Организации'] = q("ВЫБРАТЬ Наименование, Префикс ИЗ Справочник.Организации", 50)
out['Счётчики'] = q(
    "ВЫБРАТЬ "
    "(ВЫБРАТЬ КОЛИЧЕСТВО(*) ИЗ Справочник.Номенклатура) КАК Номенклатура, "
    "(ВЫБРАТЬ КОЛИЧЕСТВО(*) ИЗ Справочник.Контрагенты) КАК Контрагенты, "
    "(ВЫБРАТЬ КОЛИЧЕСТВО(*) ИЗ Справочник.ФизическиеЛица) КАК ФизЛица", 1)
out['ПоследниеРеализации'] = q(
    "ВЫБРАТЬ ПЕРВЫЕ 5 Номер, Дата, ПРЕДСТАВЛЕНИЕ(Контрагент) КАК Контрагент, СуммаДокумента "
    "ИЗ Документ.РеализацияТоваровУслуг УПОРЯДОЧИТЬ ПО Дата УБЫВ", 5)
s.conn.close()
with io.open('_ask.json','w',encoding='utf-8') as f:
    json.dump(out, f, ensure_ascii=False, indent=2)
